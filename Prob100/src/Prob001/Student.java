package Prob001;

public class Student {
    private String name;
    private String no;

    public Student(java.lang.String name, java.lang.String no) {
        this.name = name;
        this.no = no;
    }

    public java.lang.String getName() {
        return name;
    }

    public void setName(java.lang.String name) {
        this.name = name;
    }

    public java.lang.String getNo() {
        return no;
    }

    public void setNo(java.lang.String no) {
        this.no = no;
    }
}
